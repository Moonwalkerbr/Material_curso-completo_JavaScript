//Métodos são propriedades que funcionam como Funções;

//Métodos servem principalmente para transformar os dados, métodos já criados pelo JavaScript, como 'toUpperCase()', 'toString()', 'join()', 'pop()', 'push()' e etc.:

let marca = "Nike"
let lista = ["Porta", "Chave", "Janela", "Luminária"]

//O método 'toUpperCase()' retorna a String em caixa alta:
console.log(marca.toUpperCase())
    //O método 'toString()' retorna em uma String os valores de um vetor:
console.log(lista.toString())
    //O método 'join()' acrescenta um elemento entre os valores do Array:
console.log(lista.join(" # "))