//'shift' e 'unshift'

//O método 'shift' remove o item do primeiro índice de um 'array. Deste modo o 'shift' cria um novo 'array':

let sacolao = ["uva", "maçã", "laranja", "limão"]
sacolao.shift()
console.log(sacolao)
    /*
        Sintaxe:
            array.push()
    */


//O método 'unshift' adiciona um item ou mais no primeiro índice de um 'array'. Deste modo também cria um novo 'array':

let banca = ["jornal", "revista", "cigarros"]
banca.unshift("chaveiro", "gibi")
console.log(banca)
    /*
        Sintaxe:
            array.pop("item", "item"...)
    */