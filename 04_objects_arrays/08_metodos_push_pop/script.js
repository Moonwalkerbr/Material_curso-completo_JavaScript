//Métodos 'push()' e 'pop()'.

//O método 'push()' adiciona um item no final de um 'array. Deste modo muda o 'length' do 'array', retornando um novo 'array':

let partes = ["janela", "porta", "mesa", "cadeiras"]
partes.push("fogão")
console.log(partes)
    /*
    Sintaxe:
        array.push(item1, item2, item3...)

    */

//O método 'pop()' remove o último item do 'array'. Deste modo muda o 'length' do 'array', retornando um novo 'array':

let rrodesh = ["Nissan", "Iyar", "Sivan", "Tammuz"]
rrodesh.pop()
console.log(rrodesh)
    /*
        Sintaxe:
            array.pop()

    */