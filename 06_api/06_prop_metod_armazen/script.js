//Os objects 'localStorage' e 'sessionStorage' dispõe, além de 'getItem()' e 'setItem()', também de outros métodos e propriedades:

//'key()':
localStorage.key(0)
    //Retorna o nome da key com index específicado como parâmetro, parâmetro sempre requerido;

//'clear()':
localStorage.clear()
    //Remove todos os items de Objetos de dados de determinado domínio;

//'length':
localStorage.length
    //Esta propriedade retorna o número de items armazenados  no 'Object Storage' dos navegadores, em um domínio específico. Pode ser usado nos Objetos 'localStorage' e 'sessionStorage';

//'removeItem()':
localStorage.removeItem("cnh")
    //Remove o item do armazenamento local especificado. Também pode ser usado nos Objetos 'localStorage' e 'sessionStorage'.