/*Arrow Function é uma nova forma de escrever uma função no ECMAScript 6. Apesar de ser um novo modo de escreve uma função, Arrow Function não pode substituir uma function */

const multiplicarPorDois = (x) => {
    return x * 2
}

console.log(multiplicarPorDois(3))

const multiplicarPorDois2 = x => x * 2

console.log(multiplicarPorDois2(4))